<?php

function plus($a, $b) {
    return $a + $b . "<br>";
}

function moins($a, $b) {
    return $a - $b . "<br>";
}

function multiplie($a, $b) {
    return $a * $b . "<br>";
}

function divise($a, $b) {
    return $a / $b . "<br>";
}


echo "<h1>" . plus(5254,651) . "</h1>";
echo moins(5254,651);
